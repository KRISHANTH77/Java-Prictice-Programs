public class OOPSBannerApp {

    // Method to print letter O
    public static void printO() {
        System.out.println(" *** ");
        System.out.println("*     *");
        System.out.println("*     *");
        System.out.println("*     *");
        System.out.println(" *** ");
    }

    // Method to print letter P
    public static void printP() {
        System.out.println("*** ");
        System.out.println("*    *");
        System.out.println("*** ");
        System.out.println("*     ");
        System.out.println("*     ");
    }

    // Method to print letter S
    public static void printS() {
        System.out.println(" ***");
        System.out.println("*     ");
        System.out.println(" ***");
        System.out.println("      *");
        System.out.println(" ***");
    }

    public static void main(String[] args) {

        printO();
        System.out.println();

        printO();
        System.out.println();

        printP();
        System.out.println();

        printS();
    }
}
