public class EvenOddChecker {

    public static void main(String[] args) {

        // Variable declaration
        int number = 10;

        // Boolean expression
        boolean isEven = number % 2 == 0;

        // Conditional statement
        if (isEven) {
            System.out.println("Number is Even");
        } else {
            System.out.println("Number is Odd");
        }

    }
}
